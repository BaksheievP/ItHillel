'use sctict'
function Calc(number) {
    this.set = (value) => number = value,
    this.sum = (value) => number = number + value,
    this.mult = (value) => number = number * value,
    this.div  =(value) => number = number / value,
    this.sub = (value) => number = number - value   
}


let calc = new Calc()

